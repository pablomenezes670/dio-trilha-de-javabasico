public class Repetição {
    public static void main(String[] args) throws Exception {
        int x = 10;
        while (x > 0){
            System.out.println(x); //Decrecente
            x--;
        }
        for (int i = 1; i <= 10; i++){
            System.out.println(i); //Crecente


        }
    }
}
