//Estrutura de condicionais simples

public class CaixaEletronico {
    public static void main(String[] args) {
        double saldo = 25.0;
        double saldoSolicitado = 17.0;

        if (saldoSolicitado < saldo);
            saldo = saldo - saldoSolicitado;

        System.out.println(saldo);
    }
}